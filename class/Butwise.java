class Butwise{
public static void main(String args[]){
int x = -100;
int y = 100;
int z = x>>2;
System.out.println("-100>>2"+ z);
z = y>>2;
System.out.println("100>>2"+ z);
z = x<<2;
System.out.println("-100<<2"+ z);
z = y<<2;
System.out.println("100<<2"+ z);
z = x>>>2;
System.out.println("-100>>>2"+ z);
z = y>>>2;
System.out.println("100>>>2"+ z);
}
}
